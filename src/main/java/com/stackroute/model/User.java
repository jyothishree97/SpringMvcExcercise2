package com.stackroute.model;

public class User {
    String username;
    String Password;


    public String getUsername() {
        return username;
    }

    public String getPassword() {
        return Password;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public void setPassword(String password) {
        Password = password;
    }
}
